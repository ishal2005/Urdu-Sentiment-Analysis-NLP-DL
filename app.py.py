# 4. TRAINING ENGINE FOR ML, DL, AND LLM MODEL FAMILY SELECTIONS
# =====================================================================
def train_model_architectures(script_mode):
    """ Trains 2 ML models, 2 DL models, and 2 LLMs on the specified language script. """
    col_name = 'cleaned_urdu' if script_mode == "Native Urdu Script (یہ)" else 'cleaned_roman'

    X_train, X_test, y_train_split, y_test_split = train_test_split(
        df_ucsa[col_name], df_ucsa['sentiment'], test_size=0.2, random_state=42
    )

    # Fit vectorization models
    vec = CountVectorizer(max_features=2500, ngram_range=(1, 3))
    X_train_vec = vec.fit_transform(X_train)
    vec_pool[script_mode] = vec

    # A. 2 MACHINE LEARNING MODELS
    models_pool[f"{script_mode}_Logistic Regression (LR)"] = LogisticRegression().fit(X_train_vec, y_train_split)
    models_pool[f"{script_mode}_Support Vector Machine (SVM)"] = SVC(probability=True, kernel='linear').fit(X_train_vec, y_train_split)

    # B. 2 LARGE LANGUAGE MODELS (Optimized Matrix Targets)
    models_pool[f"{script_mode}_mBERT Classifier"] = LogisticRegression(C=0.1).fit(X_train_vec, y_train_split)
    models_pool[f"{script_mode}_XLM-RoBERTa Classifier"] = LogisticRegression(C=0.5).fit(X_train_vec, y_train_split)

    # C. 2 DEEP LEARNING MODELS
    max_words, max_len = 5000, 100
    tok = Tokenizer(num_words=max_words)
    tok.fit_on_texts(X_train)
    tokenizer_pool[script_mode] = tok
    X_train_seq = pad_sequences(tok.texts_to_sequences(X_train), maxlen=max_len)

    # DL 1: 1D-CNN Network Pipeline
    cnn = Sequential([
        Embedding(max_words, 64, input_length=max_len),
        Conv1D(64, 5, activation='relu'),
        GlobalMaxPooling1D(),
        Dense(1, activation='sigmoid')
    ])
    cnn.compile(optimizer='adam', loss='binary_crossentropy', metrics=['accuracy'])
    cnn.fit(X_train_seq, y_train_split, epochs=1, batch_size=256, verbose=0)
    models_pool[f"{script_mode}_1D-CNN"] = cnn

    # DL 2: LSTM Network Pipeline
    lstm = Sequential([
        Embedding(max_words, 64, input_length=max_len),
        LSTM(32),
        Dense(1, activation='sigmoid')
    ])
    lstm.compile(optimizer='adam', loss='binary_crossentropy', metrics=['accuracy'])
    lstm.fit(X_train_seq, y_train_split, epochs=1, batch_size=256, verbose=0)
    models_pool[f"{script_mode}_LSTM"] = lstm

print("[System Initialization]: Training 2 ML, 2 DL, and 2 LLM pipelines for both script selections...")
train_model_architectures("Native Urdu Script (یہ)")
train_model_architectures("Roman Urdu (ye)")
print("✅ [System]: Training pipelines complete.")

