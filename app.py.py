# 2. EXPERT BILINGUAL PREPROCESSING ENGINE
# =====================================================================
def preprocess_text_smart(text, script_mode):
    """
    Intelligently switches text cleaning regex based on script type.
    Ensures Roman character phrases are preserved when Roman script is checked.
    """
    if not isinstance(text, str):
        return ""

    # Universal URL/Email cleaning arrays
    text = re.sub(r'http\S+|www\S+|https\S+', '', text, flags=re.MULTILINE)
    text = re.sub(r'\S+@\S+', '', text)
    text = re.sub(r'\d+', '', text)
    text = re.sub(r'[^\w\s]', '', text)

    if script_mode == "Native Urdu Script (یہ)":
        # Keep ONLY Arabic/Urdu unicode range chars (0600-06FF)
        text = "".join([ch for ch in text if '\u0600' <= ch <= '\u06FF' or ch.isspace()])
        stopwords = {"ہے", "ہیں", "کا", "کی", "کے", "نے", "کو", "اور", "سے", "میں"}
    else:
        # Keep standard Latin letters for Roman Urdu strings
        text = "".join([ch for ch in text if ch.isalpha() or ch.isspace()]).lower()
        stopwords = {"hai", "hain", "ka", "ki", "ke", "ne", "ko", "aur", "se", "mein"}

    text = re.sub(r'\s+', ' ', text).strip()
    # Apply Stopword constraints safely
    text = " ".join([w for w in text.split() if w not in stopwords])
    return text

