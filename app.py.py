# 5. HIGH-ACCURACY PREDICTION ROUTINE (4 Input Variable Triggers)
# =====================================================================
def runtime_bilingual_inference(text_input, script_mode, model_name, filter_threshold):
    if not text_input.strip():
        return "⚠️ Please provide textual feedback inputs.", "N/A"

    cleaned = preprocess_text_smart(text_input, script_mode)
    lookup_key = f"{script_mode}_{model_name}"

    if lookup_key not in models_pool:
        return f"Model mapping missing error: {lookup_key}", "N/A"

    model = models_pool[lookup_key]
    prediction, confidence = 1, 0.5

    # Identify framework families to handle downstream mathematical arrays
    if model_name in ["1D-CNN", "LSTM"]:
        tok = tokenizer_pool[script_mode]
        seq = pad_sequences(tok.texts_to_sequences([cleaned]), maxlen=100)
        raw_prob = float(model.predict(seq, verbose=0)[0][0])
        prediction = 1 if raw_prob >= 0.5 else 0
        confidence = raw_prob if prediction == 1 else (1.0 - raw_prob)
    else:
        vec = vec_pool[script_mode]
        vec_data = vec.transform([cleaned])
        prediction = model.predict(vec_data)[0]
        confidence = model.predict_proba(vec_data)[0][prediction]

    if (confidence * 100) < filter_threshold:
        return f"⚠️ Output suppressed by Safety Gate. Raw: {'POSITIVE' if prediction==1 else 'NEGATIVE'} ({confidence*100:.1f}%)", f"{confidence*100:.1f}%"

    verdict = "مثبت / POSITIVE VERDICT 🟢" if prediction == 1 else "منفی / NEGATIVE VERDICT 🔴"
    return verdict, f"{confidence * 100:.2f}%"

